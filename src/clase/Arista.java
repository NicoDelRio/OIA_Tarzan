package clase;

public class Arista {
	private int posNodo1;
	private int posNodo2;
	public Arista(int posNodo1, int posNodo2) {
		super();
		this.posNodo1 = posNodo1;
		this.posNodo2 = posNodo2;
	}
	public int getPosNodo1() {
		return posNodo1;
	}
	public int getPosNodo2() {
		return posNodo2;
	}

	

}
